    private void jButton6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton6MouseClicked
         try {
        administranotas ap = new administranotas("./notas.txt");
            ap.cargarArchivo();
        if (tabla1.getSelectedRow() >= 0) {
            DefaultTableModel modelo
                    = (DefaultTableModel) tabla1.getModel();
            modelo.removeRow(tabla1.getSelectedRow());
            tabla1.setModel(modelo);
            //tf_edad_maxima.setText(maxima_edad());
             String borra = ((String) modelo.getValueAt(
                            tabla1.getSelectedRow(), 0));
            for (int i = 0; i < ap.getListanota().size(); i++) {
                if(ap.getListanota().get(i).getNombrecrea().equals(borra)){
                    ap.getListanota().remove(i);
                    ap.escribirArchivo();
                }
            }
        }
       } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurrio un Error");
            e.printStackTrace();
        }  
        
    }//GEN-LAST:event_jButton6MouseClicked

